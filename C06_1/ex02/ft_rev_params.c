#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int ac, char **av)
{
	int	i;

	i = 1;
	while (ac > 1)
	{
		ft_putstr(av[ac - 1]);
		write(1, "\n", 1);
		ac--;
	}
	return (0);
}
